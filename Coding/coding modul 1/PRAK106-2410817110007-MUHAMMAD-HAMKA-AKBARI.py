a = 4
b = 8
c = 3

print(f"Variabel a bernilai {a}")
print(f"Variabel b bernilai {b}")
print(f"Variabel c bernilai {c}")

a_sama_dengan_b = a == b
b_lebih_besar_dari_c = b > c
a_tidak_sama_dengan_c = a != c

print(f"Apakah a sama dengan b ? jawabannya adalah {int(a_sama_dengan_b)}")
print(f"Apakah b lebih besar dari c ? jawabannya adalah {int(b_lebih_besar_dari_c)}")
print(f"Apakah a tidak sama dengan c ? jawabannya adalah {int(a_tidak_sama_dengan_c)}")